int string_to_key(char* string) {
    for (size_t i = 0; i < (sizeof(keystrings) / sizeof(keystrings[0])); i++) {
        if (strcmp(keystrings[i], string) == 0)
            return i;
    }
    return -1;
}

char* key_to_string(uint8_t keycode) {
    return keystrings[keycode];
}
